public class DataTypesCopyExample {
    public static void main(String[]args){

        int variable1 = 4;
        int variable2 = variable1;

        System.out.println("variable1: "+ variable1 +" and "+ "variable2: "+ variable2 );

        variable2 =15;

        System.out.println("variable1: "+ variable1 +" and "+ "variable2: "+ variable2 );


        boolean boolean1 = true;

        boolean boolean2 = boolean1;

        System.out.println("boolean1: "+ boolean1 +" and "+ "boolean2: "+ boolean2 );

        boolean2 = false;
        System.out.println("boolean1: "+ boolean1 +" and "+ "boolean2: "+ boolean2 );



    }
}
